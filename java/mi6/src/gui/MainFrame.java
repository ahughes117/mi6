    public MainFrame(GUI aPFrame, Connector aConnector) {
        super(aPFrame, aConnector, NIL);

        initComponents();
        this.addWindowListener(new WindowAdapter() {
            public void windowClosing(WindowEvent e) {
                shutdown();
            }
        });
        try {
            ArrayList<Entity> partners = Library.loadPartnerList(c);
            for(Entity e : partners) {
                Partner p = (Partner)e;
                partnerCombo.addItem(p.getPartnerID() + " - " + p.getUrl());
            }
        } catch (SQLException ex) {
            MesDial.conError(this);
            Logger.getLogger(MainFrame.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            shutdown();
            Logger.getLogger(MainFrame.class.getName()).log(Level.SEVERE, null, ex);
        }

        super.setFrameLocationCenter();
        this.setVisible(true);
    }

    private void syncIpAddresses() {
        ipDL = new IpDL (c);
        ipDL.setConnections(Library.connectionL);
        
        try {
            ipDL.syncIps();
            MesDial.saveSuccess(this);
        } catch (SQLException ex) {
            MesDial.syncError(this);
            Logger.getLogger(MainFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
